/**
 * Get available apps (filters out unavailable apps)
 */
export function getAvailableApps(isAdmin = false): AppCard[] {
  return apps.filter((app) => {
    if (!app.available) return false;
    if (app.adminOnly && !isAdmin) return false;
    return true;
  });
}
